        {

            double temp = Math.Pow((comparingInput.R - comparingReference.R), 2) + Math.Pow((comparingInput.G - comparingReference.G), 2) + Math.Pow((comparingInput.B - comparingReference.B), 2);
            temp = Math.Sqrt(temp);
