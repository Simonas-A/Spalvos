    public class ColorInfo
    {
        public Color Color { get; private set; }
        public String Name { get; private set; }        
        
        public ColorInfo(Color inputRgb, String name)
        {
            this.Color = inputRgb;
            this.Name = name;
        }

        public ColorInfo()
        {
        }

        public override string ToString()
        {
            return string.Format("Name: {0}, R: {1}, G: {2}, B: {3}", this.Name, this.Color.R, this.Color.G, this.Color.B);
        }
    }

    public abstract class ColorBase 
    {
        /// <summary>
        /// List of system Colors 
        /// </summary>
        public ColorInfo[] Colors { get; protected set; }

        /// <summary>
        /// Find the nearest color in the list
        /// </summary>
        /// <param name="input">Color to look for</param>
        /// <returns>Information about closest color</returns>
        public int GetClosestColor(Color input)
        {
            int indexOutput = 0;
            int index = 0;
            long distance = 38654705664;


            //Goes through all the Rals and calculates the distance between input RGB and Ral RGB and puts everything to the Dictionary
            foreach (var temp in Colors)
            {
                long distanceBetweenColors = DistanceBetween(input, temp.Color);

                if (distanceBetweenColors < distance)
                {
                    indexOutput = index;
                    distance = distanceBetweenColors;
                }
                index++;
            }

            return indexOutput;
        }

        private long DistanceBetween(Color comparingInput, Color comparingReference)
        {
            long temp = ((comparingInput.R - comparingReference.R) * (comparingInput.R - comparingReference.R))
                + ((comparingInput.G - comparingReference.G) * (comparingInput.G - comparingReference.G))
                + ((comparingInput.B - comparingReference.B) * (comparingInput.B - comparingReference.B));
            //long temp = (long)(Math.Pow((comparingInput.R - comparingReference.R), 2) + Math.Pow((comparingInput.G - comparingReference.G), 2) + Math.Pow((comparingInput.B - comparingReference.B), 2));
            return temp;
        }//DistanceBetween

    }
    
